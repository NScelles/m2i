package org.example.algo.cours;

public class Variables {
    /** Démo Algo **/
    public static void main(String[] args) {

        String name = "Noa";
        System.out.println("Bonjour " + name +" !");

        /// Déclaration d'une variable

        ///Syntaxe
        // type nom = affectation;

        /// Les Types Numériques (0 par défaut)
        // int entier sur 32 bits
        int age;

        // long entier sur 64 bits
        long indice;

        // nombres réels
        float distance;
        double monnaie;

        /// Le Type Booléen (false par défaut)
        boolean estMajeur;

        /// Les Types caratères et chaines de caratères
        // Type Caratère
        char lettre;

        // Type chaine de caratère
        String prenom;

        /// Type Primitif et Objet
        // Primitif : commence par une minuscule : int, double, char
        // Objet    : commence par une majuscule : String

        /// Affectation
        age = 10;
        indice = 777_777_777;
        distance = 13.3f;
        monnaie = 77.777;
        estMajeur = true;
        lettre = 'c';
        prenom = "Noa";



    }
}
